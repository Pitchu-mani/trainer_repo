#include "./../Headers/Gcd.h"

int findSum(int a, int b) { 
    return a + b; 
}

int findDifference(int a, int b) {
    return a - b; 
}

int findProduct(int a, int b) {
    return a * b; 
}

int findQuotient(int a, int b) {
    return a / b; 
}

int findRemainder(int a, int b) {
    return a % b; 
}
